public class Assignmnet4 {

	public static void main(String[] args) {
		for(int rows=1;rows<7;rows++)
	     {
	   
	       		for(int j=rows;1<=j;j--)
	       		{
	       			System.out.print(j);
	       		}

	       			for(int k=2;k<=rows;k++)
	       			{
	       				System.out.print(k);
	       			}

	       System.out.println();
	     }

	}

}